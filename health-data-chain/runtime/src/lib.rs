//! # HealthData Chain Runtime
//!
//! This runtime implements the HealthData parachain for Patient X.
//!
//! ## Overview
//!
//! The HealthData Chain handles:
//! - Health record storage and management
//! - IPFS integration for encrypted data storage (off-chain worker)
//! - Attribute-based access control (ABAC)
//! - Encryption key lifecycle management
//! - Data provenance and lineage tracking
//!
//! ## Pallets
//!
//! ### System Pallets
//! - `System`: Core system functionality
//! - `Timestamp`: Block timestamp management
//! - `Balances`: Account balances and transfers
//! - `TransactionPayment`: Fee payment mechanism
//! - `Sudo`: Superuser for development
//! - `Aura`: Block authoring (collator consensus)
//!
//! ### Parachain Pallets
//! - `ParachainSystem`: Parachain core functionality
//! - `ParachainInfo`: Parachain ID management
//! - `AuraExt`: Aura consensus extension for parachains
//! - `XcmpQueue`: Cross-chain message queue
//! - `PolkadotXcm`: XCM execution and configuration
//! - `CumulusXcm`: Cumulus-specific XCM functionality
//!
//! ### Patient X Custom Pallets
//! - `HealthRecords`: Core health record storage
//! - `IpfsIntegration`: Distributed file storage with off-chain worker
//! - `AccessControl`: Attribute-based access control (ABAC)
//! - `Encryption`: Key lifecycle management
//! - `Provenance`: Data lineage tracking

