//! # IdentityConsent Chain Runtime
//!
//! This runtime implements the IdentityConsent parachain for Patient X.
//!
//! ## Overview
//!
//! The IdentityConsent Chain handles:
//! - Decentralized identity (DID) management
//! - Granular consent policies for health data
//! - Multi-factor authentication and session management
//! - Regulatory jurisdiction compliance
//!
//! ## Pallets
//!
//! ### System Pallets
//! - `System`: Core system functionality
//! - `Timestamp`: Block timestamp management
//! - `Balances`: Account balances and transfers
//! - `TransactionPayment`: Fee payment mechanism
//! - `Sudo`: Superuser for development
//! - `Aura`: Block authoring (collator consensus)
//!
//! ### Parachain Pallets
//! - `ParachainSystem`: Parachain core functionality
//! - `ParachainInfo`: Parachain ID management
//! - `AuraExt`: Aura consensus extension for parachains
//! - `XcmpQueue`: Cross-chain message queue
//! - `PolkadotXcm`: XCM execution and configuration
//! - `CumulusXcm`: Cumulus-specific XCM functionality
//!
//! ### Patient X Custom Pallets
//! - `IdentityRegistry`: DID and user identity management
//! - `ConsentManager`: Granular consent policies
//! - `Authentication`: Role-based access control and sessions
//! - `JurisdictionManager`: Regulatory compliance

